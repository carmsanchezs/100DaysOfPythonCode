print(2 ** 3)
print(2 ** 3.)
print(2. ** 3)
print(2. ** 3.)

print(2 * 3)
print(2 * 3.)
print(2. * 3)
print(2. * 3.)

# the result produced by the division is always a float
print(6 / 3)
print(6 / 3.)
print(6. / 3)
print(6. / 3.)

print(6 // 3)
print(6 // 3.)
print(6. // 3)
print(6. // 3.)


print(6 // 4)
print(6. // 4) # rounding always goes to the lesser integer

print(6 / 4)
print(6. / 4)

print(-6 // 4)  #-2
print(6. // -4)  #-2.0 because the rounding goes toward the lesser integer value

print(14 % 4)

print(14 % 4.5)

print(-4 + 4)

print(-4. + 8)

print(-4 - 4)
print(4. - 8)
print(-1.1)
print(+2)
